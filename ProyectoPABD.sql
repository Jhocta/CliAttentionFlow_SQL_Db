--CREACION DE VISTAS

CREATE VIEW VW_MEDICO
AS
SELECT	CODIGOMED AS CODIGO, 
		(NOMMED+''+APEPATMED+''+APEMATMED) AS NOMBRE,
		(DATEDIFF(D, FECNACMED, GETDATE())/365) AS EDAD,
		TELFMED AS TELEFONO,
		CORREOMED AS 'E-MAIL',
		CASE TURNO
			WHEN 1 THEN 'MAÑANA'
			WHEN 2 THEN 'TARDE'
			WHEN 3 THEN 'NOCHE'
		END AS TURNO,
		DIRECMED+''+DISTRITO+''+PROVINCIA+''+DEPARTAMENTO AS DIRECCION,
		NOMESPECIALIDAD AS ESPECIALIDAD,
		NOMAREA AS DEPARTAMENTO
	FROM MEDICO M INNER JOIN UBIGEO U ON M.CODIGOUBIGEO = U.CODIGOUBIGEO
				  INNER JOIN ESPECIALIDAD E ON M.CODIGOESPEC = E.CODIGOESPEC
				  INNER JOIN AREA A ON M.CODIGOAREA = A.CODIGOAREA
GO

CREATE VIEW VW_CITA
AS
SELECT	NUMEROCITA AS 'NUMERO DE CITA',
		CP.CODIGOPC AS 'COPDIGO DEL PACIENTE',
		NOMPC AS 'NOMBRE DEL PACIENTE',
		NOMMED AS 'MEDICO A CARGO',
		CP.CODIGOAREA AS DEPARTAMENTO,
		FECCITA AS DIA,
		HORACITA AS HORA,
		TELFCONTACTO AS 'TELEFONO PACIENTE',
		CORREOCONTACTO AS 'EMAIL PACIENTE',
		CASE ESTADO_CITA
			WHEN 1 THEN 'PENDIENTE'
			WHEN 2 THEN 'SIN VIGENCIA'
		END AS ESTADO
	FROM CITA_PACIENTE CP INNER JOIN PACIENTE P ON CP.CODIGOPC = P.CODIGOPC
						  INNER JOIN MEDICO M ON CP.CODIGOMED = M.CODIGOMED
						  INNER JOIN AREA A ON CP.CODIGOAREA = A.CODIGOAREA
GO

CREATE VIEW VW_RECETA 
AS
SELECT	RM.NUMERORECETA AS NUMERO,
		FECRECETA AS 'FECHA DE RECETA',
		P.CODIGOPC AS PACIENTE,
		M.CODIGOMED AS MEDICO,
		DR.CODIGOMEDICAMENTO AS 'CODIGO MEDICAMENTO',
		NOMMEDICAMENTO AS 'NOMBRE MEDICAMENTO',
		PRECIOUNIT AS 'PRECIO UNITARIO',
		DESCUENTO,
		CANTIDAD,
		SUM(PRECIOUNIT*CANTIDAD) AS TOTAL,
		((SUM(PRECIOUNIT*CANTIDAD))/1.18) AS SUBTOTAL,
		((SUM(PRECIOUNIT*CANTIDAD))-((SUM(PRECIOUNIT*CANTIDAD))/1.18)) AS IGV
	FROM RECETA_MEDICA RM INNER JOIN PACIENTE P ON RM.CODIGOPC=P.CODIGOPC
						  INNER JOIN MEDICO M ON RM.CODIGOMED=M.CODIGOMED
						  INNER JOIN DETALLE_RECETA DR ON RM.NUMERORECETA= DR.NUMERORECETA
						  INNER JOIN MEDICAMENTO MD ON DR.CODIGOMEDICAMENTO = MD.CODIGOMEDICAMENTO
	GROUP BY RM.NUMERORECETA,FECRECETA,P.CODIGOPC,M.CODIGOMED,DR.CODIGOMEDICAMENTO,NOMMEDICAMENTO,PRECIOUNIT,DESCUENTO,CANTIDAD
GO

